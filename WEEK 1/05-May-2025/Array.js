// Declare an array
let fruits = ["Apple", "Banana", "Mango"];

// Access elements by index
console.log(fruits[0]); 
console.log(fruits[1]); 

// Add a new element
fruits.push("Orange"); 
console.log(fruits); 

// Remove the last element
fruits.pop();         
console.log(fruits);   

// Loop through the array
for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}
